print("Public key received from Bob:", Yb)
c2.send(str(Yd2).encode())
print("Public key sent to Bob")
Kb = power(Yb, Xd2, p)
print("Common key between Bob and Attacker:", Kb)
c2.close()
print("Connection closed with Bob")
sv2.close()
